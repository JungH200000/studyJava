package variable;

public class Var8 {
    public static void main(String[] args) {
        // 정수
        byte b = 127; // -128 ~ 127까지 저장
        short s = 32767; // -32,768 ~ 32,767까지 저장
        int i = 2147483647; //-2,147,483,648 ~ 2,147,483,647 (약 20억)까지 저장

        long l = 9223372036854775807L;
        //-9,223,372,036,854,775,808 ~ 9,223,372,036,854,775,807까지 저장

        // 실수
        float f = 10.0f;
        double d = 10.0;
    }
}